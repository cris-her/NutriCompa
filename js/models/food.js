class Food {
    constructor(name, portion, unit, energy, carbohydrates, proteins, fats, group) {
        this.name = name;
        this.portion = portion;
        this.unit = unit;
        this.energy = energy; //kcal
        this.carbohydrates = carbohydrates; //%
        this.proteins = proteins; //%
        this.fats = fats; //%
        this.group = group; 
    }
}